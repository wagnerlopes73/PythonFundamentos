a = "Ary Wagner de Oliveira Lopes"

# Indexação em Python começa com 0


# indexa o quarto caractere da string, começando pelo 0
b = a[4]

# Imprimi o caractere
print(b)

# Imprimi a partir do caractere
c = a[1:]
print(c)

# Imprimi até do caractere
d = a[:4]
print(d)

# Imprimi tudo em lower case
e = a.lower()
print(e)

