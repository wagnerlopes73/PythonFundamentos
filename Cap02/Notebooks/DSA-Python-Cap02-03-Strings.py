a = "Ary Wagner de Oliveira Lopes"

# Indexação em Python começa com 0


# indexa o quarto caractere da string, começando pelo 0
b = a[4]

# Imprimi o caractere
print(b)

# Imprimi a partir do caractere
c = a[1:]
print(c)

# Imprimi até do caractere
d = a[:4]
print(d)

# Imprimi toda a string em lower case
e = a.lower()
print(e)

# Concatena duas strings
s = " é pai do João Carlos"
x = a + s
print(x)

# Divide a string conforme o parâmetro inserido
f = a.split("W")
print(f)

